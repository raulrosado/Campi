package cu.serpro.campi.entidades;

public class Precios {

    private Integer id;
    private Integer idcampismo;
    private String tipoetapa;
    private String dos;
    private String tre;
    private String cuatro;
    private String cinco;
    private String seis;
    private String siete;
    private String ocho;

    public Precios() {
        this.id = id;
        this.idcampismo = idcampismo;
        this.tipoetapa = tipoetapa;
        this.dos = dos;
        this.tre = tre;
        this.cuatro = cuatro;
        this.cinco = cinco;
        this.seis = seis;
        this.siete = siete;
        this.ocho = ocho;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getIdcampismo() {
        return idcampismo;
    }

    public void setIdcampismo(Integer idcampismo) {
        this.idcampismo = idcampismo;
    }

    public String getTipoetapa() {
        return tipoetapa;
    }

    public void setTipoetapa(String tipoetapa) {
        this.tipoetapa = tipoetapa;
    }

    public String getDos() {
        return dos;
    }

    public void setDos(String dos) {
        this.dos = dos;
    }

    public String getTre() {
        return tre;
    }

    public void setTre(String tre) {
        this.tre = tre;
    }

    public String getCuatro() {
        return cuatro;
    }

    public void setCuatro(String cuatro) {
        this.cuatro = cuatro;
    }

    public String getCinco() {
        return cinco;
    }

    public void setCinco(String cinco) {
        this.cinco = cinco;
    }

    public String getSeis() {
        return seis;
    }

    public void setSeis(String seis) {
        this.seis = seis;
    }

    public String getSiete() {
        return siete;
    }

    public void setSiete(String siete) {
        this.siete = siete;
    }

    public String getOcho() {
        return ocho;
    }

    public void setOcho(String ocho) {
        this.ocho = ocho;
    }
}